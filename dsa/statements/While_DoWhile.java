class While_DoWhile{
   public static void main(String[] args) {
        int a=0; 
         while (a<=5) { 
             System.out.println("hello world"); 
             a++;
         }
         int b=0;
         do { 
             System.out.println("hello java");
             b++;
         } while (b<=5);

   }
}
/*
 * while - minimum number of execution 0
 * do-while - minimum number of execution 1
 */